package src.model.plant;

public abstract class AbstractPlant {
    /**
     * max health point of a certain plant
     */
    protected float maxHP;
    /**
     * current health point of a certain plant
     */
    protected float currentHP;
    /**
     * the belonging column of a certain plant
     */
    protected float xLane;
    /**
     * the belonging row of a certain plant
     */
    protected float yLane;
}
